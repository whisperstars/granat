                                    Знакомство
Бизнес-аналитика, или BI, — это общий термин, подразумевающий под собой разнообразные программные продукты и приложения, созданные для анализа первичных данных организации.
Система бизнес-анализа(BI) - это инструмент для топ-менеджмента и руководителей подразделений организации, позволяющий иметь полное представление об актуальном положении дел.
Бизнес-анализ направлен на выявление проблем бизнеса и поиск оптимальных решений.
Задачами бизнес-анализа являются:
•   Выявление в деятельности организации отклонений, закономерностей, трендов и тенденций
•   Выявления бизнес-областей предприятия, которые являются проблемными
•   Моделирование ситуации и поиск оптимального управленческого решения
•   Выявление и анализ факторов, влияющих на деятельность организации
Один из очевидных вопросов, которые возникают у клиентов,— зачем нужна BI-система, ведь в учетной системе 1С имеется блок аналитики? 
По большому счету сравнивать 1С и BI-систему несколько некорректно, уж очень разные функциональные решения представляют собой эти комплексы и рассчитаны на решение совершенно разных задач.
Речь скорее о грамотном дополнении друг друга, чем о полной замене одного другим.
При общей масштабности функционала конфигураций 1С - все они практически лишены средств анализа и представления данных.
К тому же BI-система способена консолидировать данные для анализа сразу из нескольких конфигураций, что средствами 1С сделать затруднительно, а также присовокупить к ним данные из Excel, Access, а также любых других СУБД и источников данных (в том числе самописных).
Скорость выгрузки данных стандартными механизмами 1С требует очень много времени, даже стандартные отчеты на больших объемах данных строятся долго, а сбор информации с других источников и вовсе невозможен.
Если при обработке данных аналитики используют только 1С, то в этом случае они полностью зависят от разработчиков. 
Каждый раз при возникновении потребности в новых представлениях данных - нужно ставить новую задачу и ждать пока будет готова обработка или отчет.
Отчеты в BI-системе формируются оперативно из любого объема данных, что позволит анализировать данные и принимать управленческие решения в актуальный момент времени.
Также при сравнении отчетов 1С и BI-системе нельзя не отметить удобство представления информации и визуальную составляющую.
Представление данных в BI-системе на порядок выше (грубо говоря - в 1С никаких визуальных представлений вообще не реализовано).
В BI-системе доступны диаграммы, гистограммы, графики, таблицы, представления информации на карте и тд.
К тому же поиск данных в BI-системе (прямой и косвенный) - возможен по любому срезу данных, что в 1С тоже затруднено.
В общем вывод можно сделать такой: 1С и BI-система - два комплекса с разным функционалом и не являются конкурентами, при взаимодействии, решая каждый свой круг задач, скорее будут удачно и эффективно дополнять друг друга,давая руководителям и аналитикам возможность глубокого анализа деятельности бизнеса и реальной оценке рабочих процессов для принятия наиболее эффективных решений.
Исходя из опыта, наличие используемой BI-системы в компании говорит о грамотности и дальновидности ее руководителя, о наличии в организации четкой стратегии развития и строгого контроля деятельности подразделений.
