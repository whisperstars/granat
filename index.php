                                    Применение
Бизнес-аналитика -это ключ к пониманию собственного бизнеса. Внедрение систем отчетности является следующим шагом развития современной компании вслед за внедрением учетных систем.
Система бизнес-анализа позволяет наглядно отображать данные и взаимодействовать с ними любыми удобными способами. При этом возможности пользователя не ограничены предопределенными уровнями детализации, статичными графиками или заранее настроенными отчетами.
BI-система работает с уже имеющимися у вас ресурсами, не изменяя их, а просто забирая необходимую для анализа информацию.
Области применения BI-системы

Управление продажами

•   -Анализ продаж
•   -Анализ клиентов
•   -Прибыльность продукции


Управление закупками
•   Анализ спроса 
•   Анализ закупок 
•   Исполнительность поставщиков 
•   Управление запасами и складами


Управление ассортиментом
•   Анализ портфеля продуктов 
•   Управление продуктами
•   стоимостной анализ

Управление производством

•   Календарное планирование
•   Управление производством
•   Управление качеством
•   Анализ обслуживания оборудования


Управление финансами

•   Консолидированная фин. отчетность
•   Детальный анализ Прибылей и Убытков
•   Управление рисками 

